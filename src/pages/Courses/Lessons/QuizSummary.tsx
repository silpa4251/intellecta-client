import React from "react";

const QuizSummary = ({ lessons }) => {
  console.log(lessons);
  const totalScore = lessons.quiz.reduce((total , value)=> total + value.score, 0)
  

  return (
    <div className="fixed h-screen w-screen bg-amber-400 z-20">
      <h2>Quiz Summary</h2>
      <h1>{lessons.title}</h1>
      <div className="bg-gray-300 w-44 h-2 rounded-r-lg rounded-l-lg">
        <div
          className={`bg-green-600 h-2 rounded-l-lg rounded-r-lg`}
          style={{ width: `${totalScore}%` }}
        ></div>
      </div>
    </div>
  );
};

export default QuizSummary;
